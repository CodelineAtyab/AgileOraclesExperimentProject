  CREATE OR REPLACE EDITIONABLE TRIGGER "TRG_ADMISSIONS_BIU" 
BEFORE INSERT OR UPDATE ON admissions
FOR EACH ROW
DECLARE
   v_room_status rooms.status%TYPE;
BEGIN
   IF :NEW.status = 'Discharged' AND :NEW.discharge_date IS NULL THEN
      RAISE_APPLICATION_ERROR(-20001,'A discharged patient must have a discharge date.');
   END IF;

   IF :NEW.status = 'Admitted' THEN
      SELECT status INTO v_room_status FROM rooms WHERE room_id = :NEW.room_id;
      IF v_room_status = 'Occupied' AND
         (UPDATING AND :OLD.room_id = :NEW.room_id) THEN
         NULL;
      ELSIF v_room_status = 'Occupied' THEN
         RAISE_APPLICATION_ERROR(-20002,'Selected room is already Occupied.');
      END IF;
   END IF;
END;
/
ALTER TRIGGER "TRG_ADMISSIONS_BIU" ENABLE;
  CREATE OR REPLACE EDITIONABLE TRIGGER "TRG_ADMISSIONS_ROOM_SYNC" 
AFTER INSERT OR UPDATE ON admissions
FOR EACH ROW
BEGIN
   IF :NEW.status = 'Admitted' THEN
      UPDATE rooms SET status = 'Occupied' WHERE room_id = :NEW.room_id;
   ELSIF :NEW.status = 'Discharged' THEN
      UPDATE rooms SET status = 'Available' WHERE room_id = :NEW.room_id;
   END IF;
END;
/
ALTER TRIGGER "TRG_ADMISSIONS_ROOM_SYNC" ENABLE;
  CREATE OR REPLACE EDITIONABLE TRIGGER "TRG_CHECK_ROOM_OCCUPIED" 
BEFORE INSERT ON admissions
FOR EACH ROW
DECLARE
  v_status VARCHAR2(20);
BEGIN
  SELECT status INTO v_status FROM rooms WHERE room_id = :NEW.room_id;
  IF v_status = 'Occupied' THEN
    RAISE_APPLICATION_ERROR(-20001, 'This room is already occupied. Please choose another room.');
  END IF;
END;
/
ALTER TRIGGER "TRG_CHECK_ROOM_OCCUPIED" ENABLE;

